#include "Class_for_crypto_algorithm.h"

void TransformText(const string& stext)
{
	SourseText soursetext;
	
	for (int i = 0; i < stext.length(); i++) {
		int q = stext[i];
		int dcrC = q; //приведение к целочисленному типу данных 
		soursetext.soursetext.push_back(dcrC); //добавляем его в вектор 
	}
}